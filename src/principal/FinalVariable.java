package principal;

public class FinalVariable {
    public static void main (String[] args) {
        final int UP = 0;
        final int DOWN = 1;
        final float PI = 3.14f;
        final boolean RUN = true;
        final char A = 'A';
        final String CONST = "love for forever";
        System.out.println("\nConstante int: "+UP);
        System.out.println("Constante float: "+PI);
        System.out.println("Constante boolean: "+RUN);
        System.out.println("Constante char: "+A);
        System.out.println("Constante string: "+CONST);
    }
}
